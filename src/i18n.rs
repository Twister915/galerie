use std::collections::HashMap;

pub type Translations = HashMap<String, String>;
pub type AllTranslations = HashMap<String, Translations>;

/// Language info for configuration.
#[derive(Debug, Clone)]
pub struct LanguageInfo {
    pub code: &'static str,
    pub name: &'static str,
}

/// Returns all supported languages with their display names.
pub fn all_supported_languages() -> Vec<LanguageInfo> {
    vec![
        LanguageInfo { code: "en", name: "English" },
        LanguageInfo { code: "zh_CN", name: "简体中文" },
        LanguageInfo { code: "es", name: "Español" },
        LanguageInfo { code: "fr", name: "Français" },
        LanguageInfo { code: "nl", name: "Nederlands" },
        LanguageInfo { code: "de", name: "Deutsch" },
        LanguageInfo { code: "uk", name: "Українська" },
        LanguageInfo { code: "ru", name: "Русский" },
        LanguageInfo { code: "ja", name: "日本語" },
        LanguageInfo { code: "ar", name: "العربية" },
        LanguageInfo { code: "hi", name: "हिन्दी" },
        LanguageInfo { code: "he", name: "עברית" },
        LanguageInfo { code: "it", name: "Italiano" },
        LanguageInfo { code: "ko", name: "한국어" },
    ]
}

/// Get translations for all supported languages.
pub fn get_all_translations() -> AllTranslations {
    let mut all = HashMap::new();
    all.insert("en".to_string(), translations_en());
    all.insert("zh_CN".to_string(), translations_zh_cn());
    all.insert("es".to_string(), translations_es());
    all.insert("fr".to_string(), translations_fr());
    all.insert("nl".to_string(), translations_nl());
    all.insert("de".to_string(), translations_de());
    all.insert("uk".to_string(), translations_uk());
    all.insert("ru".to_string(), translations_ru());
    all.insert("ja".to_string(), translations_ja());
    all.insert("ar".to_string(), translations_ar());
    all.insert("hi".to_string(), translations_hi());
    all.insert("he".to_string(), translations_he());
    all.insert("it".to_string(), translations_it());
    all.insert("ko".to_string(), translations_ko());
    all
}

